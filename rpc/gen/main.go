// Package main generates synckit's exact wire-suite identity.
package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/hex"
	"encoding/json"
	"errors"
	"flag"
	"fmt"
	"go/format"
	"io"
	"os"
	"path/filepath"
	"runtime"
)

const (
	suite   = "com.yasyf.synckit.rpc"
	version = 1
)

func main() {
	check := flag.Bool("check", false, "fail when generated files differ")
	flag.Parse()
	root := moduleRoot()
	schema, err := os.ReadFile(filepath.Join(root, "rpc", "schema.json")) //nolint:gosec // Fixed generator-owned path.
	if err != nil {
		panic(err)
	}
	canonical, err := canonicalJSON(schema)
	if err != nil {
		panic(err)
	}
	var identity struct {
		Suite   string `json:"suite"`
		Version int    `json:"version"`
	}
	if err := json.Unmarshal(canonical, &identity); err != nil {
		panic(err)
	}
	if identity.Suite != suite || identity.Version != version {
		panic(fmt.Sprintf("rpc/gen: schema identity is %s/v%d, want %s/v%d", identity.Suite, identity.Version, suite, version))
	}
	digest := sha256.Sum256(canonical)
	fingerprint := hex.EncodeToString(digest[:])
	wireBuild := fmt.Sprintf("%s/%s/v%d", suite, fingerprint, version)
	source, err := format.Source([]byte(fmt.Sprintf(`// Code generated by rpc/gen; DO NOT EDIT.

package rpc

const Version uint16 = %d
const SchemaFingerprint = %q
const WireBuild = %q
`, version, fingerprint, wireBuild)))
	if err != nil {
		panic(err)
	}
	output := filepath.Join(root, "rpc", "identity_gen.go")
	if *check {
		current, err := os.ReadFile(output) //nolint:gosec // Fixed generator-owned path.
		if err != nil || !bytes.Equal(current, source) {
			fmt.Fprintln(os.Stderr, "rpc/identity_gen.go is stale; run go generate ./rpc")
			os.Exit(1)
		}
		return
	}
	if err := os.WriteFile(output, source, 0o644); err != nil { //nolint:gosec // Generated source is intentionally readable.
		panic(err)
	}
}

func canonicalJSON(source []byte) ([]byte, error) {
	decoder := json.NewDecoder(bytes.NewReader(source))
	decoder.UseNumber()
	var schema any
	if err := decoder.Decode(&schema); err != nil {
		return nil, fmt.Errorf("decode schema: %w", err)
	}
	var trailing any
	if err := decoder.Decode(&trailing); err != io.EOF {
		return nil, errors.New("decode schema: trailing JSON value")
	}
	canonical, err := json.Marshal(schema)
	if err != nil {
		return nil, fmt.Errorf("canonicalize schema: %w", err)
	}
	return canonical, nil
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("rpc/gen: caller path unavailable")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}
