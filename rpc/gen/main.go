// Package main generates synckit's exact wire-suite identity.
package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/hex"
	"encoding/json"
	"errors"
	"flag"
	"fmt"
	"go/format"
	"io"
	"os"
	"path/filepath"
	"runtime"
)

const (
	suite       = "com.yasyf.synckit.rpc"
	onceSuite   = "com.yasyf.synckit.rpc-once"
	version     = 1
	onceVersion = 1
)

func main() {
	check := flag.Bool("check", false, "fail when generated files differ")
	flag.Parse()
	root := moduleRoot()
	schema, err := readCanonical(filepath.Join(root, "rpc", "schema.json"))
	if err != nil {
		panic(err)
	}
	identity, err := schemaIdentity(schema)
	if err != nil {
		panic(err)
	}
	if identity.Suite != suite || identity.Version != version {
		panic(fmt.Sprintf("rpc/gen: schema identity is %s/v%d, want %s/v%d", identity.Suite, identity.Version, suite, version))
	}
	digest := sha256.Sum256(schema)
	fingerprint := hex.EncodeToString(digest[:])
	wireBuild := fmt.Sprintf("%s/%s/v%d", suite, fingerprint, version)

	onceSchema, err := readCanonical(filepath.Join(root, "rpc", "once_schema.json"))
	if err != nil {
		panic(err)
	}
	onceIdentity, err := schemaIdentity(onceSchema)
	if err != nil {
		panic(err)
	}
	if onceIdentity.Suite != onceSuite || onceIdentity.Version != onceVersion {
		panic(fmt.Sprintf("rpc/gen: once schema identity is %s/v%d, want %s/v%d", onceIdentity.Suite, onceIdentity.Version, onceSuite, onceVersion))
	}
	onceInput := append(append(append([]byte(nil), onceSchema...), 0), wireBuild...)
	onceDigest := sha256.Sum256(onceInput)
	onceFingerprint := hex.EncodeToString(onceDigest[:])
	onceWireBuild := fmt.Sprintf("%s/%s/v%d", onceSuite, onceFingerprint, onceVersion)
	source, err := format.Source([]byte(fmt.Sprintf(`// Code generated by rpc/gen; DO NOT EDIT.

package rpc

const Version uint16 = %d
const SchemaFingerprint = %q
const WireBuild = %q
const OnceVersion uint16 = %d
const OnceSchemaFingerprint = %q
const OnceWireBuild = %q
`, version, fingerprint, wireBuild, onceVersion, onceFingerprint, onceWireBuild)))
	if err != nil {
		panic(err)
	}
	output := filepath.Join(root, "rpc", "identity_gen.go")
	if *check {
		current, err := os.ReadFile(output) //nolint:gosec // Fixed generator-owned path.
		if err != nil || !bytes.Equal(current, source) {
			fmt.Fprintln(os.Stderr, "rpc/identity_gen.go is stale; run go generate ./rpc")
			os.Exit(1)
		}
		return
	}
	if err := os.WriteFile(output, source, 0o644); err != nil { //nolint:gosec // Generated source is intentionally readable.
		panic(err)
	}
}

type identity struct {
	Suite   string `json:"suite"`
	Version int    `json:"version"`
}

func readCanonical(path string) ([]byte, error) {
	schema, err := os.ReadFile(path) //nolint:gosec // Fixed generator-owned path.
	if err != nil {
		return nil, err
	}
	return canonicalJSON(schema)
}

func schemaIdentity(schema []byte) (identity, error) {
	var value identity
	if err := json.Unmarshal(schema, &value); err != nil {
		return identity{}, err
	}
	return value, nil
}

func canonicalJSON(source []byte) ([]byte, error) {
	decoder := json.NewDecoder(bytes.NewReader(source))
	decoder.UseNumber()
	var schema any
	if err := decoder.Decode(&schema); err != nil {
		return nil, fmt.Errorf("decode schema: %w", err)
	}
	var trailing any
	if err := decoder.Decode(&trailing); err != io.EOF {
		return nil, errors.New("decode schema: trailing JSON value")
	}
	canonical, err := json.Marshal(schema)
	if err != nil {
		return nil, fmt.Errorf("canonicalize schema: %w", err)
	}
	return canonical, nil
}

func moduleRoot() string {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		panic("rpc/gen: caller path unavailable")
	}
	return filepath.Clean(filepath.Join(filepath.Dir(file), "..", ".."))
}
